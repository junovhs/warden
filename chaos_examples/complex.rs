fn complex_logic(x: i32) {
    if x == 1 { println!("1"); }
    else if x == 2 { println!("2"); }
    else if x == 3 { println!("3"); }
    else if x == 4 { println!("4"); }
    else if x == 5 { println!("5"); }
    else if x == 6 { println!("6"); }
    else if x == 7 { println!("7"); }
    else if x == 8 { println!("8"); }
    else { println!("9"); }
}
